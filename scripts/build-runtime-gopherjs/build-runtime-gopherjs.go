package main

import (
	"go/build"
	"os"
	"path"
	"path/filepath"

	esbuild "github.com/evanw/esbuild/pkg/api"
	gbuild "github.com/gopherjs/gopherjs/build"
	"github.com/pkg/errors"
)

// NOTE: these "scripts" will be migrated to forge jobs eventually.

// BuildOptions are the GopherJS build options.
var BuildOptions = gbuild.Options{
	CreateMapFile: true,
	Verbose:       true,
	// Minify: true,
	Color: true,
	BuildTags: []string{
		"js",
		"gopherjs",
		"gc",
		"purego",
	},
}

func execBuild() error {
	build.Default.GOOS = "linux"

	// make tmp gopath
	workDir, err := os.Getwd()
	if err != nil {
		return err
	}

	// build path to output
	repoRoot := filepath.Join(workDir, "../../")
	runtimeDir := path.Join(repoRoot, "entrypoint/browser")
	runtimeDir, err = filepath.Abs(runtimeDir)
	if err != nil {
		return err
	}

	os.Stderr.WriteString("Generating runtime.js\n")
	res := esbuild.Build(esbuild.BuildOptions{
		AbsWorkingDir: runtimeDir,
		Banner: map[string]string{
			"js": "// Auto-generated by build-runtime-gopherjs",
		},
		Bundle:      true,
		Define:      map[string]string{"require": "undefined"},
		EntryPoints: []string{"runtime-js.ts"},
		Format:      esbuild.FormatIIFE,
		LogLevel:    esbuild.LogLevelDebug,
		Outfile:     filepath.Join(runtimeDir, "runtime-js.js"),
		Write:       true,
		// Inject: []string{wasmExecFile},
	})
	if len(res.Errors) != 0 {
		for _, err := range res.Errors {
			os.Stderr.WriteString(err.Text + "\n")
		}
		return errors.Errorf("esbuild failed with %d errors", len(res.Errors))
	}
	os.Stderr.WriteString("\n")

	os.Stderr.WriteString("Starting gopherjs build...\n")
	// list of packages to build
	args := []string{"github.com/aperturerobotics/bldr/entrypoint/browser"}

	// build the js
	runtimeOut := path.Join(runtimeDir, "runtime-gopherjs.js")

	// hack: disable version check
	os.Setenv("GOPHERJS_SKIP_VERSION_CHECK", "true")

	opts := BuildOptions
	sess, err := gbuild.NewSession(&opts)
	if err != nil {
		return err
	}

	xctx := gbuild.NewBuildContext(sess.InstallSuffix(), opts.BuildTags)
	pkgs, err := xctx.Match(args)
	if err != nil {
		return errors.Wrapf(err, "failed to expand patterns: %v", args)
	}
	if len(pkgs) != 1 {
		return errors.Errorf("expected %d packages but got %d", 1, len(pkgs))
	}

	pkgPath := pkgs[0]
	pkg, err := xctx.Import(pkgPath, repoRoot, 0)
	if err != nil {
		return err
	}
	if !pkg.IsCommand() {
		return errors.Errorf("expected package with main function: %s", pkg.ImportPath)
	}

	ar, err := sess.BuildPackage(pkg)
	if err != nil {
		return err
	}

	err = sess.WriteCommandPackage(ar, runtimeOut)
	if err != nil {
		return err
	}

	return nil
}

func main() {
	err := execBuild()
	if err != nil {
		os.Stderr.WriteString(err.Error())
		os.Stderr.WriteString("\n")
		os.Exit(1)
		return
	}
	os.Stdout.WriteString("built runtime with gopherjs\n")
}
