package main

import (
	"context"
	"os"
	"path"
	"path/filepath"
	"runtime"

	"github.com/aperturerobotics/controllerbus/util/exec"
	esbuild "github.com/evanw/esbuild/pkg/api"
	"github.com/pkg/errors"
	"github.com/sirupsen/logrus"
)

func execBuild() error {
	ctx := context.Background()
	log := logrus.New()
	log.SetLevel(logrus.DebugLevel)
	le := logrus.NewEntry(log)

	// make tmp gopath
	workDir, err := os.Getwd()
	if err != nil {
		return err
	}

	repoRoot := filepath.Join(workDir, "../../")
	runtimeDir := path.Join(repoRoot, "target/browser")
	runtimeDir, err = filepath.Abs(runtimeDir)
	if err != nil {
		return err
	}
	runtimeOut := path.Join(runtimeDir, "runtime.wasm")
	if _, err := os.Stat(runtimeOut); !os.IsNotExist(err) {
		err = os.Remove(runtimeOut)
		if err != nil {
			return err
		}
	}

	// go version
	_ = exec.StartAndWait(ctx, le, exec.ExecGoCompiler("version"))

	os.Stderr.WriteString("Generating runtime.js\n")
	goRootDir := runtime.GOROOT()
	wasmExecFile := path.Join(goRootDir, "misc/wasm/wasm_exec.js")
	if _, err := os.Stat(wasmExecFile); err != nil {
		return errors.Wrapf(err, "cannot find wasm_exec.js in go root: %s", wasmExecFile)
	}
	res := esbuild.Build(esbuild.BuildOptions{
		AbsWorkingDir: runtimeDir,
		Banner: map[string]string{
			"js": "// Auto-generated by build-runtime-wasm",
		},
		Bundle:      true,
		Define:      map[string]string{"require": "undefined"},
		EntryPoints: []string{"runtime-wasm.ts"},
		Format:      esbuild.FormatIIFE,
		Inject:      []string{wasmExecFile},
		LogLevel:    esbuild.LogLevelDebug,
		Outfile:     filepath.Join(runtimeDir, "runtime-wasm.js"),
		Write:       true,
	})
	if len(res.Errors) != 0 {
		for _, err := range res.Errors {
			os.Stderr.WriteString(err.Text + "\n")
		}
		return errors.Errorf("esbuild failed with %d errors", len(res.Errors))
	}
	os.Stdout.WriteString("\n")

	os.Stderr.WriteString("Starting go wasm build...\n")
	ecmd := exec.ExecGoCompiler("build", "-v", "-ldflags", "-s -w", "-o", runtimeOut)
	ecmd.Env = append(ecmd.Env, "GOOS=js", "GOARCH=wasm")
	ecmd.Dir = runtimeDir
	return exec.StartAndWait(ctx, le, ecmd)
}

func main() {
	err := execBuild()
	if err != nil {
		os.Stderr.WriteString(err.Error())
		os.Stderr.WriteString("\n")
		os.Exit(1)
		return
	}
	os.Stdout.WriteString("built runtime with wasm\n")
}
