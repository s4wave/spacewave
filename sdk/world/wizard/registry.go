package s4wave_wizard

// LookupObjectWizard returns the ObjectWizard definition for a type ID.
// Returns nil if the type is not a registered creatable type.
func LookupObjectWizard(typeID string) *ObjectWizard {
	for _, w := range ObjectWizards {
		if w.GetTypeId() == typeID {
			return w
		}
	}
	return nil
}

