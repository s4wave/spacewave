package entrypoint_electron_bundle

import (
	"context"
	"os"
	"path"
	"path/filepath"

	util_esbuild "github.com/aperturerobotics/bldr/esbuild"
	bundle "github.com/aperturerobotics/bldr/web/entrypoint/browser/bundle"
	"github.com/aperturerobotics/util/exec"
	esbuild "github.com/evanw/esbuild/pkg/api"
	"github.com/sirupsen/logrus"
)

// ElectronDefine returns the define mapping for Electron.
func ElectronDefine() map[string]string {
	return map[string]string{
		"BLDR_IS_ELECTRON": "true",
	}
}

// EsbuildLogLevel is the log level when bundling the electron bundle.
var EsbuildLogLevel = esbuild.LogLevelInfo

// ElectronBuildOpts are general options for building for Electron.
func ElectronBuildOpts(repoRoot string, minify bool) esbuild.BuildOptions {
	opts := bundle.BrowserBuildOpts(repoRoot, minify)
	opts.Define = ElectronDefine()
	opts.External = []string{"electron"}
	opts.LogLevel = EsbuildLogLevel
	return opts
}

// BuildServiceWorkerBundle builds specifically the service worker files.
func BuildServiceWorkerBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	return bundle.BuildServiceWorkerBundle(le, repoRoot, buildDir, minify)
}

// BuildPreloadBundle builds the web renderer bundle files.
func BuildPreloadBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	le.Debug("generating electron preload bundle")
	opts := ElectronBuildOpts(repoRoot, minify)
	opts.Define = ElectronDefine()
	opts.EntryPointsAdvanced = nil
	opts.EntryNames = ""
	opts.EntryPoints = []string{
		"web/electron/main/preload.ts",
	}
	opts.Outfile = path.Join(buildDir, "preload.js")
	opts.Platform = esbuild.PlatformNode
	opts.Write = true
	if !minify {
		opts.Sourcemap = esbuild.SourceMapLinked
	} else {
		opts.Sourcemap = esbuild.SourceMapNone
	}
	res := esbuild.Build(opts)
	return util_esbuild.BuildResultToErr(res)
}

// BuildMainBundle builds the electron Main bundle files.
func BuildMainBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	le.Debug("generating electron main bundle")
	opts := ElectronBuildOpts(repoRoot, minify)
	opts.Define = ElectronDefine()
	opts.EntryPointsAdvanced = nil
	opts.EntryNames = ""
	opts.EntryPoints = []string{
		"web/electron/main/index.ts",
	}
	opts.Outfile = path.Join(buildDir, "index.js")
	opts.Platform = esbuild.PlatformNode
	opts.Write = true
	if !minify {
		opts.Sourcemap = esbuild.SourceMapLinked
	} else {
		opts.Sourcemap = esbuild.SourceMapNone
	}
	res := esbuild.Build(opts)
	return util_esbuild.BuildResultToErr(res)
}

// BuildRendererBundle builds the web renderer bundle files.
func BuildRendererBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	le.Debug("generating web renderer bundle")

	// index.html
	distSrcDir := path.Join(repoRoot, "web")
	indexHtmlPath := path.Join(distSrcDir, "index.html")
	ihtml, err := os.ReadFile(indexHtmlPath)
	if err != nil {
		return err
	}
	rendererHtmlOut := path.Join(buildDir, "index.html")
	err = os.WriteFile(rendererHtmlOut, ihtml, 0644)
	if err != nil {
		return err
	}

	// entrypoint
	webEntrypointOut := path.Join(buildDir, "entrypoint")
	opts := ElectronBuildOpts(repoRoot, minify)
	opts.Outdir = webEntrypointOut
	opts.EntryPointsAdvanced = nil
	opts.EntryNames = ""
	opts.Define = ElectronDefine()
	opts.EntryPoints = []string{
		"web/entrypoint/entrypoint.tsx",
	}
	opts.Write = true
	if !minify {
		opts.Sourcemap = esbuild.SourceMapLinked
	}
	res := esbuild.Build(opts)
	return util_esbuild.BuildResultToErr(res)
}

// BuildRuntimeBundle copies all runtime files including runtime-electron.ts
func BuildRuntimeBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	// runtime
	runtimeOut := path.Join(buildDir, "runtime")
	err := os.MkdirAll(runtimeOut, 0755)
	if err != nil {
		return err
	}
	runtimeDir := path.Join(repoRoot, "web/entrypoint/electron")
	runtimeDir, err = filepath.Abs(runtimeDir)
	if err != nil {
		return err
	}

	opts := esbuild.BuildOptions{
		AbsWorkingDir: runtimeDir,
		Banner: map[string]string{
			"js": "// Auto-generated by build-electron",
		},
		Bundle:      true,
		EntryPoints: []string{"runtime-electron.ts"},
		Format:      esbuild.FormatDefault,
		LogLevel:    EsbuildLogLevel,
		Outfile:     filepath.Join(runtimeOut, "runtime-electron.js"),
		Write:       true,
	}
	if !minify {
		opts.Sourcemap = esbuild.SourceMapInline
	}
	res := esbuild.Build(opts)
	return util_esbuild.BuildResultToErr(res)
}

// BuildBrowserBundle builds and outputs the web & service worker files.
func BuildBrowserBundle(le *logrus.Entry, repoRoot, buildDir string, minify bool) error {
	err := os.MkdirAll(buildDir, 0755)
	if err != nil {
		return err
	}

	// service worker
	if err := BuildServiceWorkerBundle(le, repoRoot, buildDir, minify); err != nil {
		return err
	}

	// preload
	if err := BuildPreloadBundle(le, repoRoot, buildDir, minify); err != nil {
		return err
	}

	// main
	if err := BuildMainBundle(le, repoRoot, buildDir, minify); err != nil {
		return err
	}

	// renderer bundle
	if err := BuildRendererBundle(le, repoRoot, buildDir, minify); err != nil {
		return err
	}

	// runtime bundle
	if err := BuildRuntimeBundle(le, repoRoot, buildDir, minify); err != nil {
		return err
	}

	return nil
}

// BuildAsar builds the app asar using the @electron/asar tool.
//
// asarBinPath should be the path to the asar binary.
// buildDir should be pre-prepared using BuildBrowserBundle.
// outPath should be the path to the output .asar file
func BuildAsar(ctx context.Context, le *logrus.Entry, asarBinPath, buildDir, outPath string) error {
	cmd := exec.NewCmd(asarBinPath, "pack", buildDir, outPath)
	return exec.StartAndWait(ctx, le, cmd)
}
